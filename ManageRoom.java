    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String RoomNumber=jTextField1.getText();
        int i=0;
        try{
            Connection con=ConnectionProvider.getcon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from room"+RoomNumber+"");
            while(rs.next())
            {
                i=1;
                if(rs.getString(3).equals("Booked"))
                {
                    JOptionPane.showMessageDialog(null,"This Room is Booked.");
                    clear();
                }
                else
                {
                   jTextField1.setEditable(false);
                   jTextField1.setForeground(Color.red);
                   jTextField1.setBackground(Color.pink);
                   if(rs.getString(2).equals("yes"))
                   {
                       jCheckBox2.setSelected(true);
                   }
                   else
                   {
                       jCheckBox2.setSelected(false);
                   }
                    
                }
            if(i==0){
                JOptionPane.showMessageDialog(null,"This Room Doesn't Exist.");
            }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

