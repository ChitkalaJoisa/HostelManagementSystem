    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String RoomNumber=jTextField2.getText();
        String activate;
        String RoomStatus="Not Booked";
        if(jCheckBox1.isSelected()){
            activate="Yes";
        }
        else
            activate="No";
        try
        {
            Connection con=ConnectionProvider.getcon();
            JOptionPane.showMessageDialog(null,"About to book the room-Step1");
            PreparedStatement ps =con.prepareStatement("insert into room values(?,?,?)");
            JOptionPane.showMessageDialog(null,"About to book the room-Step2");
            ps.setString(1,RoomNumber);  
            ps.setString(2,activate);
            ps.setString(3,RoomStatus);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"Sucessfully Updated.");
            TableDetails();
            clear();
        
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e); 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

